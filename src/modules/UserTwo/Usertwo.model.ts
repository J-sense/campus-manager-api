import { model, Schema } from 'mongoose';
import { TUserTwo } from './Usertwo.interface';

const UserSchema = new Schema<TUserTwo>(
  {
    id: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    needsPasswordChange: {
      type: Boolean,
      default: true,
    },
    role: {
      type: String,
      enum: ['student', 'faculty', 'admin'],
    },
    status: {
      type: String,
      enum: ['in-progress,blocked'],
      default: 'in-progress',
    },
    isDeleted: {
      type: Boolean,
      default: false,
    },
  },
  {
    timestamps: true,
  },
);
export const UserTwo = model<TUserTwo>('UserTwo', UserSchema);
